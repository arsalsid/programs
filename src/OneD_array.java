import java.util.Scanner;
public class OneD_array {
    public static void main(String[] args) {

        int[] onedimens = {20, 25, 35};
        System.out.println(onedimens[0]);

        //Multi dimension Array
        int [][] multidimenseArray = {{20, 25, 35} , {40, 45, 50} , {55, 60, 65}};
        System.out.println(multidimenseArray[0][1]);
        System.out.println(multidimenseArray[1][1]);
    }
}
